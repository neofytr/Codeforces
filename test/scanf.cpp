#include <bits/stdc++.h>

using namespace std;

int main()
{
    int t, a, b;
    scanf("%d\n", &t);

    while (t--)
    {
        scanf("%d %d\n", &a, &b);

        // operate
    }

    int a, b;

    // stop when both a and b are zero
    while (scanf("%d %d\n", &a, &b), a || b)
    {
        // operate
    }

    int a, b;

    // stop when EOF reached

    while (scanf("%d %d\n", &a, &b) != EOF)
    {
        // operate
    }

    return 0;
}